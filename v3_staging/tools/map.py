#!/usr/bin/env python3
"""
map.py — P5 design mapper. Loads the HFT design payload onto the routed container.

PURPOSE (P5 — Configuration / Load):
  Instantiate the design payload (modules: MAC, NIC, DOM, TAI, timeframe, indicators, adapter)
  onto the cast container (P2) via the router output (P3). The design is NOT placed on an empty
  blank — it is LOADED/CONFIGURED onto the interconnected container.

INPUTS:
  - device/container.json        P2 cast (tile array, native clocks/RAM/IO)
  - device/routes.json           P3 router output (PIP config, single-driver gate PASS)
  - design_payload.yaml or CONFIG env var (founder decision: which modules to load)

OUTPUTS:
  - device/design_netlist.json   instantiated cells + nets (design circuit mapped to container)
  - device/design_routes.json    merged PIP config (design-specific + container routes)
  - device/design_bitstream.bin  (P6 input; generated by bitstream emitter)

DESIGN PAYLOAD (P5 open decision, resolved at runtime):
  The payload is DECIDED SEPARATELY (not here). This tool:
  1. Reads config/payload.json or environment variable DESIGN_PAYLOAD
  2. For each module (adapter, nic, tai, dom, timeframe, indicators):
     - Load module HDL spec (if not pre-realized in P1)
     - Instantiate onto container via router
     - Merge routes into design_routes.json
  3. Emit design_netlist.json + design_routes.json
  4. Gate: module-contract (2i), index-doctrine (2h), build-purity (2j), arithmetic (2b)

SKELETON STATUS (2026-06-14):
  - Placeholder: reads config/design_payload.md and instantiates a test module
  - Full implementation blocked on founder decision: which design modules?
  - When decided: replace test_module() with real module instantiation from library.json

CODE STYLE:
  Python 3.9+, functional, matches catalog.py structure.
  All paths relative to repo root (use HERE/ROOT pattern).
  No hard-coded module names; read from config.

DEPENDENCIES:
  - route.py (needs container routes from device/routes.json)
  - library.json (P1 output; primitive library)
  - design_payload.md (config; to be created by founder)
"""
import sys, os, json, argparse
HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
DEVICE = os.path.join(ROOT, "device")
CONFIG = os.path.join(ROOT, "config")

def load_json(path, default=None):
    """Load JSON file, return default if missing."""
    try:
        return json.load(open(path))
    except (FileNotFoundError, json.JSONDecodeError):
        return default

def load_container():
    """Load P2 cast container (tile array, native clocks/RAM/IO)."""
    return load_json(os.path.join(DEVICE, "container.json"), {})

def load_router_output():
    """Load P3 router output (PIP config, verified single-driver)."""
    routes = load_json(os.path.join(DEVICE, "routes.json"), {})
    if not routes:
        print("ERROR: device/routes.json not found. Run route.py (P3) first.", file=sys.stderr)
        return None
    return routes

def load_library():
    """Load P1 primitive library (physical elements + config mappings)."""
    lib = load_json(os.path.join(DEVICE, "library.json"), {})
    if not lib:
        print("WARN: library.json not found (P1 incomplete?). Using empty library.", file=sys.stderr)
    return lib

def load_design_payload_config():
    """
    Load design payload configuration. Priority:
      1. DESIGN_PAYLOAD environment variable (JSON or path to file)
      2. config/design_payload.md
      3. Empty list (no modules — bare container only)

    Returns: dict with 'modules' list, each with 'name' and optional 'params'.
    """
    env_payload = os.environ.get("DESIGN_PAYLOAD")
    if env_payload:
        try:
            return json.loads(env_payload)
        except json.JSONDecodeError:
            if os.path.isfile(env_payload):
                return load_json(env_payload, {})

    config_path = os.path.join(CONFIG, "design_payload.md")
    if os.path.isfile(config_path):
        # TODO: parse YAML frontmatter from design_payload.md
        # For now, return empty config (founder decision pending).
        return {"modules": []}

    return {"modules": []}

