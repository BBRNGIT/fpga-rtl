def build_design_netlist(container, library, payload_config):
    """
    Instantiate all design modules onto the container.

    Returns: dict with 'cells', 'nets', 'modules' listing.
    """
    all_cells = []
    all_nets = []
    cell_id = 0
    modules_instantiated = []

    for module_spec in payload_config.get("modules", []):
        cells, nets, cell_id = instantiate_module(module_spec, container, library, cell_id)
        all_cells.extend(cells)
        all_nets.extend(nets)
        modules_instantiated.append({
            "name": module_spec.get("name"),
            "cells": len(cells),
            "nets": len(nets),
        })

    # If no modules specified, still emit the bare structure
    if not all_cells:
        print("INFO: no modules instantiated; emitting bare container structure", file=sys.stderr)

    return {
        "cells": all_cells,
        "nets": all_nets,
        "modules": modules_instantiated,
        "total_cells": len(all_cells),
        "total_nets": len(all_nets),
    }

def merge_design_routes(container_routes, design_netlist):
    """
    Merge design-specific PIP routes with container routes.

    The router (P3) has already routed container nets via the INT-tile fabric.
    Now we add design-specific routes for design nets, ensuring:
      - single-driver-per-wire gate holds
      - no shorts (design nets don't collide with container nets)

    Returns: dict with merged routes, gate status.
    """
    routes = container_routes.copy() if container_routes else {
        "grid": [64, 64],
        "routes": [],
        "total_pips": 0,
    }

    design_routes = []
    for net in design_netlist.get("nets", []):
        # TODO: run router on design net endpoints (when modules are realized)
        # For now, emit empty design routes
        pass

    routes["design_nets_routed"] = len(design_routes)
    routes["design_routes"] = design_routes
    routes["total_pips"] += sum(r.get("hops", 0) for r in design_routes)

    # Re-check single-driver gate
    all_wires = [tuple(p["tile"]) + (p["out"], p["track"])
                 for r in routes.get("routes", [])
                 for p in r.get("pips", [])]
    for dr in design_routes:
        all_wires.extend([tuple(p["tile"]) + (p["out"], p["track"])
                          for p in dr.get("pips", [])])

    single_driver = len(all_wires) == len(set(all_wires))
    routes["single_driver"] = single_driver
    routes["no_shorts"] = single_driver

    return routes

def run(payload_source=None):
    """
    Main P5 flow: instantiate design modules onto the routed container.

    Exit codes:
      0 = success (gates PASS)
      1 = validation error (spec is wrong — fix design_payload.md)
      2 = internal/transient error (missing P2/P3 artifacts, retry)
    """
    # Load P3 router output (must exist)
    container_routes = load_router_output()
    if not container_routes:
        print("ERROR: P3 routes not found. P5 blocked on P3 completion.", file=sys.stderr)
        return 2

    # Load P2 container
    container = load_container()
    if not container:
        print("WARN: P2 container not found. Using minimal stub.", file=sys.stderr)
        container = {"grid": [64, 64], "tiles": []}

    # Load P1 library
    library = load_library()

    # Load design payload config
    payload_config = load_design_payload_config()

    # Instantiate design modules
    design_netlist = build_design_netlist(container, library, payload_config)

    # Merge design routes with container routes
    merged_routes = merge_design_routes(container_routes, design_netlist)

    # Gate: single-driver-per-wire (from P3, still holds after design routing)
    if not merged_routes.get("single_driver"):
        print("GATE FAIL: single-driver-per-wire", file=sys.stderr)
        return 1

    if not merged_routes.get("no_shorts"):
        print("GATE FAIL: no-shorts", file=sys.stderr)
        return 1

    # Emit design_netlist.json
    os.makedirs(DEVICE, exist_ok=True)
    netlist_path = os.path.join(DEVICE, "design_netlist.json")
    json.dump(design_netlist, open(netlist_path, "w"), indent=2)
    print(f"design_netlist.json: {design_netlist['total_cells']} cells, "
          f"{design_netlist['total_nets']} nets, {len(design_netlist['modules'])} modules",
          file=sys.stderr)

    # Emit design_routes.json
    routes_path = os.path.join(DEVICE, "design_routes.json")
    json.dump(merged_routes, open(routes_path, "w"), indent=2)
    print(f"design_routes.json: {merged_routes.get('total_pips', 0)} total PIPs, "
          f"{merged_routes.get('design_nets_routed', 0)} design nets routed",
          file=sys.stderr)

    print(f"P5 SUCCESS: {design_netlist['total_cells']} design cells loaded onto container", file=sys.stderr)
    return 0

if __name__ == "__main__":
    ap = argparse.ArgumentParser(description="P5 design mapper: load payload onto routed container")
    ap.add_argument("--payload", type=str, help="design_payload.yaml or JSON file (overrides env/config)")
    ap.add_argument("--dry-run", action="store_true", help="validate only, no output")
    args = ap.parse_args()

    sys.exit(run(payload_source=args.payload))
