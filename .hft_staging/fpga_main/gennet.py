#!/usr/bin/env python3
"""gennet.py — generic FPGA-BLANK generator: <board>.net.json -> <board>_gen.h.

A blank is pure device reference (blank-enforcement law): the internally-connected
fabric. Emits ONLY:
  * the fabric register-file geometry (capacity, word type) — the interconnect,
  * the address-space geometry constants (sizes, NOT addresses),
  * the clock-domain frequency table (descriptive constants),
  * the I/O + resource budget constants (real-part facts),
  * a zero-init (power-off reset of the fabric).

NO module allocation, NO logic, NO tick, NO clock loop — modules are installed by
the install tool (phase 4) after system-wide addressing (phase 3). This is a TOOL;
its output (*_gen.h) is never hand-edited.

Usage: python3 gennet.py <board>.net.json > <board>_gen.h
"""
import json
import sys


def main():
    net_path = sys.argv[1] if len(sys.argv) > 1 else None
    if not net_path:
        sys.stderr.write("usage: gennet.py <board>.net.json > <board>_gen.h\n")
        sys.exit(2)
    net = json.load(open(net_path))
    if net.get("kind") != "fpga_blank":
        sys.stderr.write("gennet: not an fpga_blank netlist\n")
        sys.exit(2)

    dev = net["device"]
    DEV = dev.upper()
    fab = net["fabric"]
    geo = net["address_space_geometry"]

    out = []
    out.append(f"/* GENERATED by gennet.py from {net_path} — DO NOT EDIT BY HAND.")
    out.append(f" * {net.get('board', dev)}: FPGA BLANK — the internally-connected fabric reference.")
    out.append(f" * Real part: {net.get('part_ref', '')}")
    out.append(" * Pure device reference: fabric geometry + clock/I-O/resource tables. NO module")
    out.append(" * allocation, NO logic, NO addresses — install (phase 4) seats modules after")
    out.append(" * system-wide addressing (phase 3). The fabric register file IS the interconnect. */")
    out.append(f"#ifndef {DEV}_GEN_H")
    out.append(f"#define {DEV}_GEN_H")
    out.append("#include <stdint.h>")
    out.append("")
    out.append(f"typedef uint64_t {dev}_word_t;")
    out.append("")
    out.append("/* ---- fabric geometry (the interconnect) ---- */")
    out.append(f"#define {DEV}_FABRIC_REGS {fab['registers']}u")
    out.append(f"#define {DEV}_WORD_BITS {fab['word_bits']}u")
    out.append("")
    out.append("/* ---- address-space GEOMETRY (sizes only; addresses are assigned in phase 3) ---- */")
    out.append(f"#define {DEV}_ADDR_SPACE_SIZE {hex(geo['size'])}u")
    out.append(f"#define {DEV}_SLOT_ALIGNMENT {hex(geo['slot_alignment'])}u")
    out.append(f"#define {DEV}_CDC_REGION_SIZE {hex(geo['cdc_region_size'])}u")
    out.append("")
    out.append("/* ---- clock-domain table (descriptive; oscillator modules own the clocks) ---- */")
    for cn, c in net.get("clock_domain_table", {}).items():
        out.append(f"#define {DEV}_CLK_{cn.upper()}_HZ {c['frequency_hz']}u")
    out.append("")
    out.append("/* ---- I/O + resource budget (real-part facts) ---- */")
    for k, v in net.get("io_geometry", {}).items():
        out.append(f"#define {DEV}_IO_{k.upper()} {v}u")
    for rn, r in net.get("resource_budget", {}).items():
        out.append(f"#define {DEV}_RES_{rn.upper()}_CAPACITY {r['capacity']}u")
    out.append("")
    out.append(f"/* {dev}_init: zero the fabric (power-off reset). A blank runs no clock and")
    out.append(" * owns no logic; this is the only state operation it defines. */")
    out.append(f"static inline void {dev}_init({dev}_word_t *f) {{")
    out.append(f"    {dev}_word_t i = 0ULL;")
    out.append(f"    for (i = 0ULL; i < {DEV}_FABRIC_REGS; i = i + 1ULL) {{ f[i] = 0ULL; }}")
    out.append("}")
    out.append("")
    out.append(f"#endif /* {DEV}_GEN_H */")
    sys.stdout.write("\n".join(out) + "\n")


if __name__ == "__main__":
    main()
