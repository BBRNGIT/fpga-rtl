#!/usr/bin/env python3
"""gennet.py — generic passive-bus generator: <bus>.net.json -> <bus>_gen.h.

A passive lock-free BARRIER bus (wire, dom_bus): addressed lanes only — NO clock,
NO compute, NO latch. Emits just the address map (one slot per lane, declaration
order) + lane count + a zero-init. The producer DEPOSITS into the lanes (sole
writer); consumers SAMPLE them. This is a TOOL; its output (*_gen.h) is never
hand-edited.

Usage: python3 gennet.py <bus>.net.json > <bus>_gen.h
"""
import json
import sys


def main():
    net_path = sys.argv[1] if len(sys.argv) > 1 else "dom_bus.net.json"
    with open(net_path) as f:
        net = json.load(f)
    if net.get("kind") != "passive_bus":
        sys.stderr.write("gennet: not a passive_bus netlist\n"); sys.exit(2)

    dev = net["device"]; DEV = dev.upper(); pfx = DEV
    nodes = [n["name"] for n in net.get("bus_nodes", [])]
    if len(nodes) != len(set(nodes)):
        sys.stderr.write("gennet: duplicate bus lane names\n"); sys.exit(3)
    addr = {nm: i for i, nm in enumerate(nodes)}

    out = []
    out.append(f"/* GENERATED by gennet.py from {net_path} — DO NOT EDIT BY HAND.")
    out.append(f" * {dev}: passive lock-free BARRIER bus — addressed lanes only, no clock,")
    out.append(" * no compute, no latch. Producer DEPOSITS (sole writer); consumers SAMPLE.")
    out.append(" * A module never reads another's registers directly; this bus is the medium. */")
    out.append(f"#ifndef {DEV}_GEN_H")
    out.append(f"#define {DEV}_GEN_H")
    out.append("#include <stdint.h>")
    out.append("")
    out.append(f"typedef uint64_t {dev}_word_t;")
    out.append(f"#define {pfx}_REG_COUNT {len(nodes)}u")
    for nm in nodes:
        out.append(f"#define {pfx}_{nm} {addr[nm]}u")
    out.append("")
    out.append(f"/* {dev}_init: zero the passive bus (power-off reset). No clock runs here. */")
    out.append(f"static inline void {dev}_init({dev}_word_t *b) {{")
    out.append(f"    {dev}_word_t i = 0ULL;")
    out.append(f"    for (i = 0ULL; i < {pfx}_REG_COUNT; i = i + 1ULL) {{ b[i] = 0ULL; }}")
    out.append("}")
    out.append("")
    out.append(f"#endif /* {DEV}_GEN_H */")
    sys.stdout.write("\n".join(out) + "\n")


if __name__ == "__main__":
    main()
