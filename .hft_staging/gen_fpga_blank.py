GITIGNORE = "test_fpga_blank\n*.dSYM/\n"


def emit_component(prof, comp_dir):
    os.makedirs(comp_dir, exist_ok=True)
    files = {
        "gennet.py": GENNET,
        "validate.py": VALIDATE,
        "Makefile": MAKEFILE,
        "test_fpga_blank.c": TEST,
        ".gitignore": GITIGNORE,
    }
    for name, content in files.items():
        with open(os.path.join(comp_dir, name), "w") as f:
            f.write(content)
    sys.stderr.write(f"gen_fpga_blank: emitted complete fileset -> {comp_dir}/ "
                     f"({', '.join(files)})\n"
                     f"gen_fpga_blank: now run `make test` inside {comp_dir}/ "
                     f"(emit -> validate -> gen -> test)\n")


def main():
    args = [a for a in sys.argv[1:] if not a.startswith("--")]
    if not args:
        die("usage: gen_fpga_blank.py <part_profile.yaml> [--emit-component <dir>] "
            "[> fpga_blank.net.json]")
    prof = load_profile(args[0])

    if "--emit-component" in sys.argv:
        i = sys.argv.index("--emit-component")
        if i + 1 >= len(sys.argv):
            die("--emit-component needs a directory")
        emit_component(prof, sys.argv[i + 1])
        return

    json.dump(build_netlist(prof), sys.stdout, indent=2)
    sys.stdout.write("\n")


if __name__ == "__main__":
    main()
